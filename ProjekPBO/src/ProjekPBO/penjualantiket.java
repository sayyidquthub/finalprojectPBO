    private void tbtransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbtransaksiMouseClicked
        // TODO add your handling code here:
        int baris = tbtransaksi.rowAtPoint(evt.getPoint());
        String Nomor_Kursi = tbtransaksi.getValueAt(baris, 0).toString();
        NoKursi.setText(Nomor_Kursi);
        String Nama_Penumpang = tbtransaksi.getValueAt(baris, 1).toString();
        nama.setText(Nama_Penumpang);
        String Jumlah_Beli = tbtransaksi.getValueAt(baris, 2).toString();
        JumlahBeli.setText(Jumlah_Beli);
        String Total_Bayar = tbtransaksi.getValueAt(baris, 3).toString();
        TotalBayar.setText(Total_Bayar);
        String Jurusan = tbtransaksi.getValueAt(baris, 4).toString();
        jJurusan.setSelectedItem(Jurusan);
        String Jenis = tbtransaksi.getValueAt(baris, 5).toString();
        jJenis.setSelectedItem(Jenis);
        String Harga = tbtransaksi.getValueAt(baris, 6).toString();
        jHarga.setText(Harga);
    }//GEN-LAST:event_tbtransaksiMouseClicked

