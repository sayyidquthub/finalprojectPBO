    public void TampilkanData(){
        DftTbModel_transaksi = new DefaultTableModel();
        DftTbModel_transaksi.addColumn("Nomor Kursi");
        DftTbModel_transaksi.addColumn("Nama Penumpang");
        DftTbModel_transaksi.addColumn("Jumlah Beli");
        DftTbModel_transaksi.addColumn("Total Bayar");
        DftTbModel_transaksi.addColumn("Jurusan");
        DftTbModel_transaksi.addColumn("Jenis");
        DftTbModel_transaksi.addColumn("Harga");
        
        tbtransaksi.setModel(DftTbModel_transaksi);
        Connection conn = koneksi.getConnection();
        try{
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from tbdata";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while(res.next()){
                DftTbModel_transaksi.addRow(new Object[]{
                res.getString("Nomor_Kursi"),
                res.getString("Nama_Penumpang"),
                res.getString("Jumlah_Beli"),
                res.getString("Total_Bayar"),
                res.getString("Jurusan"),
                res.getString("Jenis"),
                res.getString("Harga"),
                });
            }
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
